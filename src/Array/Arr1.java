package Array;

public class Arr1 {

	public static void main(String[] args)
	{
		String fruit[]=new String[4];
		// Declaration and Instantiation
		fruit[0]="Apple";
		fruit[1]="Banana";
		fruit[2]="Orange";
		fruit[3]="Grapes";
		// Initialization
		for(int i=0;i<fruit.length;i++)
		{
			System.out.println(fruit[i]);
		}
	}
}
