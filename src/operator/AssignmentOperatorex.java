package operator;

public class AssignmentOperatorex 
{

	public static void main(String args[])
	{  
		int d=10;  
		int e=20;  
		d+=4;//d=d+4 (d=10+4)  14
		e-=4;//e=e-4 (e=20-4)  16
		System.out.println(d);  
		System.out.println(e);
		int a=10;  
		a+=3;//10+3 = 13  
		System.out.println(a);  
		a-=4;//13-4  = 9
		System.out.println(a);  
		a*=2;//9*2  = 18
		System.out.println(a);  
		a/=2;//18/2  = 9
		System.out.println(a);  
	}
}  
